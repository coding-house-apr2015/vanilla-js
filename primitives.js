// 5 primitive types
// string, number, boolean, null, undefined

// strings have either single or double quotes
var a = 'hello';
var b = "world";
console.log(a, b);

// numbers are either integer or floating point
var c = 3;
var d = 3.14;
console.log(c, d);

// boolean are either true or false
var e = true;
var f = 3 < 4;
var g = 'a' === 'b';
console.log(e, f, g);

// null and undefined
var h = null;
console.log(h);
var i;
console.log(i);
